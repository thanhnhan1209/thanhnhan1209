    private void btnAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductActionPerformed
         // TODO add your handling code here:
String maSP = txtMaSanPham.getText().trim();
String tenSP = txtTenSanPham.getText().trim();
String xuatxu = txtXuatXu.getText().trim();
String phanLoai = (String) cbxloaisp.getSelectedItem();

int soluong = 0;
int dongia = 0;
int trangThai = 1; // Mặc định sản phẩm đang hoạt động

// Kiểm tra tên sản phẩm
if (tenSP.length() > 100 || !tenSP.matches("[\\p{L}0-9\\s\\-()]+")) {
    JOptionPane.showMessageDialog(this, "Tên sản phẩm không hợp lệ! Không quá 100 ký tự, không chứa ký tự đặc biệt.");
    return;
}

// Kiểm tra số lượng
try {
    soluong = Integer.parseInt(txtSoLuong.getText());
    if (soluong < 0) throw new NumberFormatException();
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng hợp lệ (>= 0)!");
    return;
}

// Kiểm tra đơn giá
try {
    dongia = Integer.parseInt(txtDonGia.getText());
    if (dongia < 0) throw new NumberFormatException();
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "Vui lòng nhập đơn giá hợp lệ (>= 0)!");
    return;
}

// Kiểm tra các trường bắt buộc
if (maSP.isEmpty() || tenSP.isEmpty() || xuatxu.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
    return;
}

try {
    // Tạo đối tượng sản phẩm
    SanPham sanpham = new SanPham(maSP, tenSP, soluong, dongia, phanLoai, xuatxu, trangThai);

    // Gọi DAO để thêm vào cơ sở dữ liệu
    SanPhamDAO.getInstance().insert(sanpham);
    this.dispose();
    JOptionPane.showMessageDialog(this, "Thêm sản phẩm thành công!");
    owner.loadDataToTable();
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Thêm sản phẩm thất bại!\n" + e.getMessage());
}
        
    }//GEN-LAST:event_btnAddProductActionPerformed

